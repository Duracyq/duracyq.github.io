function applyTranslations(lang) {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.dataset.i18n;
    const translation = translations[lang][key];
    if (!translation) return;
    if (el.dataset.i18nHtml === "true") {
      el.innerHTML = translation;
    } else {
      el.textContent = translation;
    }
  });

  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  const toggle = document.querySelector(".lang-toggle");
  if (toggle) {
    const label = toggle.querySelector(".lang-label");
    const flag = toggle.querySelector(".flag");
    if (label) label.textContent = lang === "pl" ? "Polski" : "English";
    if (flag) flag.textContent = lang === "pl" ? "🇵🇱" : "🇬🇧";
  }

  localStorage.setItem("lang", lang);
}

function setupLanguageSwitcher() {
  const dropdown = document.querySelector(".lang-dropdown");
  const toggle = document.querySelector(".lang-toggle");
  const options = document.querySelectorAll(".lang-option");
  const initialLang = localStorage.getItem("lang") || "pl";
  applyTranslations(initialLang);

  if (toggle && dropdown) {
    toggle.addEventListener("click", (e) => {
      e.stopPropagation();
      const expanded = dropdown.classList.toggle("open");
      toggle.setAttribute("aria-expanded", expanded ? "true" : "false");
    });
  }

  options.forEach((btn) => {
    btn.addEventListener("click", (e) => {
      e.stopPropagation();
      const lang = btn.dataset.lang;
      applyTranslations(lang);
      const menu = document.querySelector(".menu-links");
      const icon = document.querySelector(".hamburger-icon");
      if (menu && menu.classList.contains("open")) {
        menu.classList.remove("open");
        if (icon) icon.classList.remove("open");
      }
      if (dropdown) {
        dropdown.classList.remove("open");
        if (toggle) toggle.setAttribute("aria-expanded", "false");
      }
    });
  });

  document.addEventListener("click", () => {
    if (dropdown) {
      dropdown.classList.remove("open");
      if (toggle) toggle.setAttribute("aria-expanded", "false");
    }
  });
}

function toggleMenu() {
  const menu = document.querySelector(".menu-links");
  const icon = document.querySelector(".hamburger-icon");
  menu.classList.toggle("open");
  icon.classList.toggle("open");
}

document.addEventListener("DOMContentLoaded", () => {
  setupLanguageSwitcher();
});
